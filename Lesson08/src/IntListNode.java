public class IntListNode {

    //attributes
    public int value;
    public IntListNode next;

    //constructor(s)
    public IntListNode(int value){
        this.value = value;
    }

    //getters & setters
    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public IntListNode getNext() {
        return next;
    }

    public void setNext(IntListNode next) {
        this.next = next;
    }

}

